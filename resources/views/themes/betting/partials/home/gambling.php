      <div class="section__title">
          <h4>
            How to change?
          </h4>
      </div>
      <div class="main__table main__table__tennis main__table__cricket">
        <div class="mt-3">
          <p><strong>There are always REASONS why people gamble. And this is the key to prevent and change the root cause of the habit.
            In one way or another, those reasons trigger the itching feeling to go and gamble. Are there alternatives to what you can do? There are – and you can choose one based on the trigger which you or your relative might have:
            </strong></p>
          <p><strong>• Solving money problems</strong></p>
          <p>Gambling companies are not there to make people rich and solve their financial problems. You must acknowledge this. The odds are always stacked against you, so it’s a far more effective way to seek financial aid in the form of loans from your local banks than illusively expect your next bet to land a big win and solve all the problems.
          </p>
          <p><strong>• Need for an excitement or rush of adrenaline</strong></p>
          <p>Go wild with a sport or a challenging hobby, such as mountain biking, rock climbing, or GoKart Racing.
          </p>
          <p><strong>• Lack of social life, because you are reserved or shy person</strong></p>
          <p>Spending time in online-gambling seems like fun, but instead create a hobby which will force you to go outdoors and engage in a group activity - join a social circle or a club, connect with family and friends, volunteer, find new friends.
          </p>
          <p><strong>• Avoiding or escaping thoughts about problems and/or painful feelings</strong></p>
          <p>Many people turn to addictions to escape or avoid their life problems. Divorce, for example, might make you willing to go and have a drink or play some slots just to forget about it. Instead, try therapy sessions, book an appointment with a psychologist or simply talk your problems through with close friends – let them help you.
          </p>
          <p><strong>• Boredom or loneliness</strong></p>
          <p>Find a hobby – something you’re passionate about, such as art, music, sports, or books. Nowadays there are plentiful means on social media to find individuals with similar interests. You do not have to be alone.
          </p>
          <p><strong>• Merely relaxing after a stressful day</strong></p>
          <p>From as little as 15 minutes of daily physical exercise relieves a lot of mental stress. Cycling or walking from work could be a good alternative if you cannot get yourself to the gym.
          </p>
          <p class="py-2">For a relative or a friend of a problem gambler</p>
          <p><strong>If there is a gambling problem in your home, don’t wait for the gambler to admit it.
            One of the crucial points for helping problem gamblers is to have the support and understanding of their family and friends. The last thing you would want them to think is that you are judging and confronting them. This will cause a detachment and might pull them even more towards gambling when they start feeling misunderstood, unwanted or pressured.
            </strong></p>
          <p class="py-2">As with any other addiction – you must accept and understand that the decision to quit gambling has to be their own. As much as you may want to, and as painful as it might be to watch your close ones going through addiction – you cannot forcefully make someone stop gambling. However, you can encourage them to seek help, support them in their efforts, protect yourself, and take any mentions of suicide seriously.
          </p>
          <p class="py-2">How can you do it? Seek help for yourself!
            This is extremely important to support oneself first, in order to be able to support others. You can learn about more effective ways of coping with the gambler and the gambling problem on a practical, day-to-day basis. You can learn techniques to enable you to deal with the extreme levels of stress this situation is creating. This may even motivate the gambler toward seeking help for him or herself.
          </p>
