      <div class="section__title">
          <h4>
          How to spot?
          </h4>
      </div>
      <div class="main__table main__table__tennis main__table__cricket">
        <div class="mt-3">
          <p><strong>Gambling addiction has no obvious physical signs or symptoms like there are in drug or alcohol addiction. Sometimes your friend, relative or even yourself might not be aware of what is happening. But, as with any other addiction, the hallmark sign of a gambling problem is that you feel you cannot stop. How can you spot the tendency in yourself or someone close to you?</strong></p>
          <p class="py-2">Common signs of addiction include, but are not limited to, the following:</p>
          <p><strong>• Feeling the need to be secretive about your gambling. </strong></p>
          <p>You might gamble in secret or lie about how much you gamble, you might feel that others fail to understand you, or that you will surprise them with a big win.</p>
          <p><strong>• Having trouble controlling your gambling. </strong></p>
          <p>Once you start gambling, can you walk away? When you win – do you walk away or continue to play in hopes to win even more? If you loose, do you continue to play until the last coin, in hopes to win your losses back?</p>
          <p><strong>• Gambling when you cannot afford to.  </strong></p>
          <p>You may gamble until you’ve spent your last money, and then move on to funds you are not supposed to spend – to pay bills, credit cards, or budget for your children. You may feel compelled to borrow, sell, or even steal, to try your luck once more to win back your losses.</p>
          <p><strong>• Having family and friends worried about you.  </strong></p>
          <p>Denial keeps problem gambling going. If friends and family are worried, listen to them carefully. It’s not a sign of weakness to accept help. Many older gamblers are reluctant to reach out to their adult children if they’ve gambled away their inheritance, but it’s never too late to make changes for the better.</p><br>
          <p>If you think you may have a gambling problem, ask yourself whether you would feel okay to stop gambling right now. If you feel rather anxious, or as if you shouldn’t stop yet, chances are you might be suffering from a gambling addiction.</p>
        </div>
